function monthPrinter(monthNumeral){
    if (monthNumeral == 1) {
        console.log("January")
    } else if (monthNumeral == 2) {
        console.log("February")
    } else if (monthNumeral == 3) {
        console.log("March")
    } else if (monthNumeral == 4) {
        console.log("April")
    } else if (monthNumeral == 5) {
        console.log("May")
    } else if (monthNumeral == 6) {
        console.log("June")
    } else if (monthNumeral == 7) {
        console.log("July")
    } else if (monthNumeral == 8) {
        console.log("August")
    } else if (monthNumeral == 9) {
        console.log("September")
    } else if (monthNumeral == 10) {
        console.log("October")
    } else if (monthNumeral == 11) {
        console.log("November")
    } else if (monthNumeral == 12) {
        console.log("December")
    } else {
        console.log("Error!")
    }
}